public class Basquete {
	private String equipeVencedora;
	private String mandante;
	private String visitante;
	private double mediaPontos;
	private double mediaFaltas;

	public String getEquipeVencedora() {
		return equipeVencedora;
	}

	public String getMandante() {
		return mandante;
	}

	public String getVisitante() {
		return visitante;
	}

	public double getMediaPontos() {
		return mediaPontos;
	}

	public double getMediaFaltas() {
		return mediaFaltas;
	}

	public void setEquipeVencedora(String equipeVencedora) {
		this.equipeVencedora = equipeVencedora;
	}

	public void setMandante(String mandante) {
		this.mandante = mandante;
	}

	public void setVisitante(String visitante) {
		this.visitante = visitante;
	}

	public void setMediaPontos(double mediaPontos) {
		this.mediaPontos = mediaPontos;
	}

	public void setMediaFaltas(double mediaFaltas) {
		this.mediaFaltas = mediaFaltas;
	}

}
